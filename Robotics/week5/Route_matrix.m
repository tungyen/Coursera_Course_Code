function time = Route_matrix(dt1,dt2,dt3)
    time = zeros(12);
    time(1,1)=1;
    time(2,1)=1;
    time(2,2)=dt1;
    time(2,3)=dt1^2;
    time(2,4)=dt1^3;
    time(3,5)=1;
    time(4,5)=1;
    time(4,6)=dt2;
    time(4,7)=dt2^2;
    time(4,8)=dt2^3;
    time(5,9)=1;
    time(6,9)=1;
    time(6,10)=dt3;
    time(6,11)=dt3^2;
    time(6,12)=dt3^3;
    time(7,2)=1;
    time(8,10)=1;
    time(8,11)=2*dt3;
    time(8,12)=3*dt3^2;
    time(9,2)=1;
    time(9,3)=2*dt1;
    time(9,4)=3*dt1^2;
    time(9,6)=-1;
    time(10,3)=2;
    time(10,4)=6*dt1;
    time(10,7)=-2;
    time(11,6)=1;
    time(11,7)=2*dt2;
    time(11,8)=3*dt2^2;
    time(11,10)=-1;
    time(12,7)=2;
    time(12,8)=6*dt2;
    time(12,11)=-2;
end